package com.xoomcode.acidmaps.error;

public class AcidMapException extends Exception {

	public AcidMapException() {
		super();
	}

	public AcidMapException(String message, Throwable cause) {
		super(message, cause);
	}

	public AcidMapException(String message) {
		super(message);
	}

	public AcidMapException(Throwable cause) {
		super(cause);
	}
	

}
